CREATE TABLE D_WALISANTRI(
    KD_WALI	        INT AUTO_INCREMENT,
    ID_USER	        INT NOT NULL,
    ID_SANTRI	    INT NOT NULL,
    ALAMAT	        VARCHAR	(250),
    KD_PROVINSI	    VARCHAR	(50),
    KD_KABUPATEN	VARCHAR	(50),
    KD_KECAMATAN	VARCHAR	(50),
    FOTO_WALI	    BLOB,
    TGL_LAHIR	    DATE,	
    TEMPAT_LAHIR	VARCHAR	(250),
    JENIS_KELAMIN	ENUM('A', 'N') DEFAULT 'A',
    KD_AGAMA        INT,

    CONSTRAINT PK_KD_WALI PRIMARY KEY (KD_WALI),
    CONSTRAINT FK_ID_USER_WALI FOREIGN KEY (ID_USER) REFERENCES MD_USER(ID_USER) ON DELETE NO ACTION,
    CONSTRAINT FK_ID_SANTRI_WALI FOREIGN KEY (ID_SANTRI) REFERENCES D_SANTRI(ID_SANTRI) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_PROVINSI_WALI FOREIGN KEY (KD_PROVINSI) REFERENCES MD_PROVINSI(KD_PROVINSI) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_KABUPATEN_WALI FOREIGN KEY (KD_KABUPATEN) REFERENCES MD_KABUPATEN(KD_KABUPATEN) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_KECAMATAN_WALI FOREIGN KEY (KD_KECAMATAN) REFERENCES MD_KECAMATAN(KD_KECAMATAN) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_AGAMA FOREIGN KEY (KD_AGAMA) REFERENCES MD_AGAMA(KD_AGAMA) ON DELETE NO ACTION
);