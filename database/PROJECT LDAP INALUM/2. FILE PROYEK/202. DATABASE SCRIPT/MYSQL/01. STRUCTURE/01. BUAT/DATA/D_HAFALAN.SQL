CREATE TABLE D_HAFALAN(
	KD_HAFALAN		INT AUTO_INCREMENT,
	ID_SANTRI		INT,
	TGL_PENILAIAN	DATE,	
	NM_SURAT		VARCHAR	(1000),
	NO_AYAT			VARCHAR	(100),
	JUZ				VARCHAR	(50),
	USTADZ			VARCHAR	(100),
	KETERANGAN		VARCHAR	(1000),
	TGL_INSERT		DATE,
	TGL_UPDATE		DATE,

	CONSTRAINT PK_KD_HAFALAN PRIMARY KEY (KD_HAFALAN),
    CONSTRAINT FK_ID_SANTRI_HAFALAN  FOREIGN KEY (ID_SANTRI) REFERENCES D_SANTRI(ID_SANTRI) ON DELETE NO ACTION
);