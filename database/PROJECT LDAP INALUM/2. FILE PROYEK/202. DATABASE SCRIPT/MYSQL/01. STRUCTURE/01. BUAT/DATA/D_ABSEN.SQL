CREATE TABLE D_ABSEN(
	KD_ABSEN		INT AUTO_INCREMENT,
	ID_SANTRI		INT,
	TGL				DATE,
	KD_STATUSABSEN	INT,
	KETERANGAN		VARCHAR	(1000),
	TGL_INSERT		DATE,
	TGL_UPDATE		DATE,

	CONSTRAINT PK_KD_ABSEN PRIMARY KEY (KD_ABSEN),
    CONSTRAINT FK_ID_SANTRI_ABSEN  FOREIGN KEY (ID_SANTRI) REFERENCES D_SANTRI(ID_SANTRI) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_STATUS_ABSEN  FOREIGN KEY (KD_STATUSABSEN) REFERENCES MD_STATUSABSEN(KD_STATUSABSEN) ON DELETE NO ACTION
);