CREATE TABLE D_SANTRI(
    ID_SANTRI	    INT AUTO_INCREMENT,	
    NAMA_SANTRI	    VARCHAR	(100),
    ALAMAT_SANTRI	VARCHAR	(1000),
    KD_PROVINSI	    VARCHAR	(50),
    KD_KABUPATEN	VARCHAR	(50),
    KD_KECAMATAN	VARCHAR	(50),
    FOTO_SANTRI	    BLOB,
    TGL_LAHIR	    DATE,
    TEMPAT_LAHIR	VARCHAR	(100),
    JENIS_KELAMIN	ENUM('L', 'P') DEFAULT 'L',
    KD_KELAS        INT,

    CONSTRAINT PK_ID_SANTRI PRIMARY KEY (ID_SANTRI),
    CONSTRAINT FK_KD_PROVINSI_SANTRI  FOREIGN KEY (KD_PROVINSI) REFERENCES MD_PROVINSI(KD_PROVINSI) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_KABUPATEN_SANTRI FOREIGN KEY (KD_KABUPATEN) REFERENCES MD_KABUPATEN(KD_KABUPATEN) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_KECAMATAN_SANTRI FOREIGN KEY (KD_KECAMATAN) REFERENCES MD_KECAMATAN(KD_KECAMATAN) ON DELETE NO ACTION,
    CONSTRAINT FK_KD_KELAS            FOREIGN KEY (KD_KELAS) REFERENCES MD_KELAS(KD_KELAS) ON DELETE NO ACTION
);