CREATE TABLE D_BACAANQURAN(
	KD_BACAAN		INT AUTO_INCREMENT,
	ID_SANTRI		INT,
	TGL_PENILAIAN	DATE,
	JUZ				VARCHAR	(50),
	NM_SURAT		VARCHAR	(1000),
	NO_AYAT			VARCHAR	(100),
	MAKHROJ			VARCHAR	(1000),
	LAFAZ			VARCHAR	(1000),
	TARTIL			VARCHAR	(1000),
	TAJWID			VARCHAR	(1000),
	USTADZ			VARCHAR	(100),
	KETERANGAN		VARCHAR	(1000),
	TGL_INSERT		DATE,
	TGL_UPDATE		DATE,

	CONSTRAINT PK_KD_BACAAN PRIMARY KEY (KD_BACAAN),
    CONSTRAINT FK_ID_SANTRI_BACAAN  FOREIGN KEY (ID_SANTRI) REFERENCES D_SANTRI(ID_SANTRI) ON DELETE NO ACTION
);