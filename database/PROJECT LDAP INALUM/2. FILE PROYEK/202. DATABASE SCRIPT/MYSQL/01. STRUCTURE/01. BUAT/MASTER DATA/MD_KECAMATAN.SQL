CREATE TABLE MD_KECAMATAN (
    KD_KECAMATAN	VARCHAR	(50) NOT NULL,
    KD_KABUPATEN	VARCHAR	(50) NOT NULL,
    NM_KECAMATAN	VARCHAR	(100),
    KETERANGAN	    VARCHAR	(250),
    STATUS	        ENUM('A', 'N') DEFAULT 'A',
    INDEX(KD_KABUPATEN),

    CONSTRAINT PK_KD_KECAMATAN PRIMARY KEY (KD_KECAMATAN),
    CONSTRAINT FK_KD_KABUPATEN FOREIGN KEY (KD_KABUPATEN) REFERENCES MD_KABUPATEN(KD_KABUPATEN) ON DELETE NO ACTION
);