CREATE TABLE MD_MENU (
    KD_MENU	    INT AUTO_INCREMENT,
    NM_MENU	    VARCHAR	(100),
    NM_LINK	    VARCHAR	(100),
    STATUS	    ENUM('A', 'N') DEFAULT 'A',
    LEVEL_MENU	VARCHAR	(50),
    KD_SUBMENU	VARCHAR	(50),
    KETERANGAN	VARCHAR	(250),

    CONSTRAINT PK_KD_MENU PRIMARY KEY (KD_MENU)
);