CREATE TABLE MD_KABUPATEN (
    KD_KABUPATEN	VARCHAR	(50) NOT NULL,
    KD_PROVINSI	    VARCHAR	(50) NOT NULL,
    NM_KABUPATEN	VARCHAR	(100),
    KETERANGAN	    VARCHAR	(250),
    STATUS	        ENUM('A', 'N') DEFAULT 'A',
    INDEX(KD_PROVINSI),
    
    CONSTRAINT PK_KD_KABUPATEN PRIMARY KEY (KD_KABUPATEN),
    CONSTRAINT FK_KD_PROVINSI FOREIGN KEY (KD_PROVINSI) REFERENCES MD_PROVINSI(KD_PROVINSI) ON DELETE NO ACTION
);