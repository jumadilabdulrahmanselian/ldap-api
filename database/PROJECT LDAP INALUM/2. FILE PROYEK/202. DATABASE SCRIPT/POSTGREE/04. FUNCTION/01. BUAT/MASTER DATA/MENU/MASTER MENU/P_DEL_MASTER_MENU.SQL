CREATE OR REPLACE FUNCTION P_DEL_MASTER_MENU(
    KD_MENU1 character varying)
RETURNS character varying AS
$BODY$
DECLARE
    CACAH NUMERIC;
    MESSAGES VARCHAR;
BEGIN
    SELECT COUNT(*) INTO CACAH FROM MD_MENU WHERE KD_MENU=KD_MENU1;
    IF CACAH > 0 THEN
        MESSAGES := '200:DATA BERHASIL DIHAPUS';
        DELETE FROM MD_MENU WHERE KD_MENU=KD_MENU1;
    ELSE
        MESSAGES := '400:TIDAK ADA DATA DITEMUKAN, ATAU MUNGKIN DATA SUDAH TERHAPUS';
    END IF;

RETURN MESSAGES;
END;
$BODY$
    LANGUAGE plpgsql VOLATILE