CREATE SEQUENCE D_DOKUMEN_KD_DOKUMEN_SEQ;

CREATE TABLE D_DOKUMEN (
	KD_DOKUMEN VARCHAR (50) PRIMARY KEY,
	NOMOR_DOKUMEN VARCHAR (100),
	JUDUL_DOKUMEN VARCHAR (500),
	KETERANGAN_DOKUMEN VARCHAR (1000),
	KD_DEPARTEMEN VARCHAR (50),
	KD_UNIT VARCHAR (50),
	TANGGAL_DOKUMEN TIMESTAMP,
	STATUS VARCHAR (5),

	FOREIGN KEY (KD_DEPARTEMEN) REFERENCES MD_DEPARTEMEN(KD_DEPARTEMEN),
	FOREIGN KEY (KD_UNIT) REFERENCES MD_UNIT(KD_UNIT)
);

ALTER SEQUENCE D_DOKUMEN_KD_DOKUMEN_SEQ OWNED BY D_DOKUMEN.KD_DOKUMEN;

